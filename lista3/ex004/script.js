var pessoa1 = Number(prompt("Digite a idade da primeira pessoa:"))
var pessoa2 = Number(prompt("Digite a idade da segunda pessoa:"))

if (pessoa1 == pessoa2) {
    alert ("As duas pessoas tem a mesma idade")
} 
else if (pessoa1 > pessoa2) {
    alert ("A primeira pessoa é mais velha")
} 
else {
    alert ("A segunda pessoa é mais velha")
}
